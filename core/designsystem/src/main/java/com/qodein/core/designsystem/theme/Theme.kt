val LocalExtendedColorScheme = staticCompositionLocalOf {
    ExtendedColorScheme(
        complementary = Color.Unspecified,
        onComplementary = Color.Unspecified,
        complementaryContainer = Color.Unspecified,
        onComplementaryContainer = Color.Unspecified,
